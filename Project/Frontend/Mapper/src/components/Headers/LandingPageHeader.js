import React from "react";

// reactstrap components
import { Button, Container } from "reactstrap";

// core components

function LandingPageHeader() {
  let pageHeader = React.createRef();

  React.useEffect(() => {
    if (window.innerWidth < 991) {
      const updateScroll = () => {
        let windowScrollTop = window.pageYOffset / 3;
        pageHeader.current.style.transform =
          "translate3d(0," + windowScrollTop + "px,0)";
      };
      window.addEventListener("scroll", updateScroll);
      return function cleanup() {
        window.removeEventListener("scroll", updateScroll);
      };
    }
  });

  return (
    <>
      <div
        style={{
          backgroundImage: "url(" + require("assets/img/grid_neon2.png") + ")"
        }}
        className="page-header"
        data-parallax={true}
        ref={pageHeader}
      >
        <div className="filter" />
        <Container>
          <div className="motto text-center">
            <h1 style={{color:"white", fontSize:60, fontWeight:"bold"}}>Traffic Jammer</h1>
            <h3 style={{color:'white', fontWeight:"bolder"}} >Closely follow traffic in your favourite city.</h3>
            <br />
            <Button
              href="https://www.youtube.com/watch?v=dQw4w9WgXcQ"
              className="btn-round mr-1"
              color="neutral"
              target="_blank"
            >
              <i className="fa fa-play" />
              Watch Tutorial
            </Button>
            <Button className="btn-round" href="/app" color="neutral" type="button">
              Use Traffic Jammer
            </Button>
          </div>
        </Container>
      </div>
    </>
  );
}

export default LandingPageHeader;
